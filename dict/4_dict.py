
my_dict = {'name': 'Alice', 'age': 30, 'city': 'New York'}


print("Iterating over keys:")
for key in my_dict:
    print(key)


print("\nIterating over values:")
for value in my_dict.values():
    print(value)


print("\nIterating over key-value pairs:")
for key, value in my_dict.items():
    print(key, ":", value)
